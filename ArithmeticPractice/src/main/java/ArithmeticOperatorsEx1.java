public class ArithmeticOperatorsEx1 {
    public static void main(String[] args) {
        System.out.printf("3 + 10 = %d\n", 3 + 10);
        System.out.printf("3 - 10 = %d\n", 3 - 10);
        System.out.printf("3 * 10 = %d\n", 3 * 10);
        System.out.printf("3 / 10 = %d\n", 3 / 10);
        System.out.printf("3 %% 10 = %d\n", 3 % 10);
    }
}
