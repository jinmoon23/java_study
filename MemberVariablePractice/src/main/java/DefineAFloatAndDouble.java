public class DefineAFloatAndDouble {
    public static void main(String[] args) {
        // float 변수 선언 및 초기화
        float f1 = 3.14f;
        float f2 = 1.23e10f;

        // double 변수 선언 및 초기화
        double d1 = 3.141592653589793;
        double d2 = 1.23e100;

        // 변수 연산
        float result1 = f1 * 2.0f;
        double result2 = d1 / 2.0;

        System.out.println(result1);
        System.out.println(result2);

    }
}
